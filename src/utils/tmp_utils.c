
#include "minishell.h"

void    print_array(t_array *arr, t_bool minus)
{
    if (minus)
        for (size_t i = 0; i < arr->size && arr->arr[i] != -1; i++)
            DN(arr->arr[i]);
    else
        for (size_t i = 0; i < arr->size; i++)
            DN(arr->arr[i]);
}

void    print_commands(t_node **commands)
{
    t_command   *cmd;
    t_node      *iter;

    iter = *commands;
    while (iter)
    {
        cmd = iter->data;
        print(cmd->cmd);
        iter = iter->next;
    }
}

void    print_tokens(t_node **tokens_list)
{
    t_node  *iter;
    t_token *token;

    iter = *tokens_list; 
    while (iter)
    {
        token = iter->data;
        printf("@> [%s]\n", (char*)(token->tok));
        iter = iter->next;
    }
}